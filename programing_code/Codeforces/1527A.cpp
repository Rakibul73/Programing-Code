#include <bits/stdc++.h> 
using namespace std;
#define fast_cin() ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL)

void solve() {
    long long n, i = 1;
    cin >> n;
    
    while (1)
    {
        if(n < i*2) {
            cout << i - 1 << "\n";
            break;
        }
        else {
            i = i * 2;
        }
    }
    return;
}

int main() {
    // Below function is A fast IO program
    fast_cin();
    int t;
    cin >> t;
    //t = 1;
    for (int i = 1; i <= t; i++) {
        solve();
    }
    
    return 0;
}